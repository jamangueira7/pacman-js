let gameLoop = () => {
    update();
    draw();
}

let update = () => {
    pacman.moveProcess();
    pacman.eat();
}

let drawFoods = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                createReact(
                    j * oneBlockSize + oneBlockSize / 3,
                    i * oneBlockSize + oneBlockSize / 3,
                    oneBlockSize / 3,
                    oneBlockSize / 3,
                    foodColor,
                );
            }
        }
    }
}

let draw = () => {
    createReact(0,0, canvas.width, canvas.height, "black");
    drawWalls();
    drawFoods();
    pacman.draw();
}

let gameInternal = setInterval(gameLoop, 1000 / fps);

let drawWalls = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 1) {
                //the it is a wall
                createReact(
                    j * oneBlockSize,
                    i * oneBlockSize,
                    oneBlockSize,
                    oneBlockSize,
                    wallColor,
                );

                if (j > 0 && map[i][j - 1] == 1) {
                    createReact(
                        j * oneBlockSize,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor,
                    );
                }

                if (j < map[0].length - 1 && map[i][j + 1] == 1) {
                    createReact(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor,
                    );
                }

                if (i > 0 && map[i - 1][j] == 1) {
                    createReact(
                        j * oneBlockSize  + wallOffset,
                        i * oneBlockSize,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor,
                    );
                }

                if (i < map.length - 1 && map[i + 1][j] == 1) {
                    createReact(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor,
                    );
                }
            }
        }
    }
}

